double raycaster_math_sin(double angle)
{
    // Keep angle inside [0, 360) so we can safely index the table
    while (angle < 0.0) {
        angle += 360.0;
    }
    while (angle >= 360.0) {
        angle -= 360.0;
    }

    // Pick the two nearest whole degrees around the input angle
    int idx0 = (int)angle;
    int idx1 = idx0 + 1;
    if (idx1 >= 360) {
        idx1 = 0;
    }

    // frac tells how far we are between idx0 and idx1
    // Example: angle=12.3 -> idx0=12, idx1=13, frac=0.3
    double frac = angle - (double)idx0;

    // Convert scaled table values back to normal doubles
    double s0 = (double)sin_table[idx0] / 1000.0;
    double s1 = (double)sin_table[idx1] / 1000.0;

    // Find a value between s0 and s1 and return it
    return s0 + (s1 - s0) * frac;
}

double raycaster_math_cos(double angle)
{
    // cos(a) = sin(a + 90)
    return raycaster_math_sin(angle + 90);
}

void raycaster_init(Raycaster *rc)
{
    // Spawn player at a safe starting location in the map
    rc->player.x = 3.5;
    rc->player.y = 1.5;
    rc->player.angle = 0;
    rc->player.speed = RAYCASTER_MOVE_STEP;
    rc->frame_count = 0;
    rc->game_running = true;
}

int raycaster_check_collision(double x, double y)
{
    // Convert world position to map cell index
    int map_x = (int)x;
    int map_y = (int)y;

    // Outside map is treated as solid wall
    if (map_x < 0 || map_x >= MAP_WIDTH || map_y < 0 || map_y >= MAP_HEIGHT) {
        return 1;
    }

    return g_raycaster_map_data[map_y][map_x];
}
